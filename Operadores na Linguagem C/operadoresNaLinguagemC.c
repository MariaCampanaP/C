#include <stdio.h>

int main(){

    printf("=============================================\n");
    printf("            TABELA DE OPERADORES EM C        \n");
    printf("=============================================\n\n");

    //Operadores Aritméticos
    printf("OPERADORES ARITMETICOS\n");
    printf("----------------------\n");
    printf("+  -> adicao\n");
    printf("-  -> subtracao\n");
    printf("*  -> multiplicacao\n");
    printf("/  -> divisao\n");
    printf("%%  -> resto da divisao (mod)\n\n");  // %% para imprimir %

    //Operadores Comparativos
    printf("OPERADORES COMPARATIVOS\n");
    printf("------------------------\n");
    printf("<   -> menor\n");
    printf(">   -> maior\n");
    printf("<=  -> menor ou igual\n");
    printf(">=  -> maior ou igual\n");
    printf("==  -> igual\n");
    printf("!=  -> diferente\n\n");

    //Operadores Lógicos
    printf("OPERADORES LOGICOS\n");
    printf("------------------\n");
    printf("&&  -> e (AND)\n");
    printf("||  -> ou (OR)\n");
    printf("!   -> nao (NOT)\n");

    return 0;

}
